<h2 class="wiki-section-header">
    Maize Links
</h2>  

    <br>
    <strong>
        <a href="http://www.maizegdb.org/">MaizeGDB
        </a>
    </strong>
    <div align="justify">MaizeGDB is the community database for biological information about the crop plant Zea mays ssp. mays. Genetic, genomic, sequence, gene product, functional characterization, literature reference, and person/organization contact information are among the datatypes accessible through this site.
    </div>

    <br>
    <strong>
        <a href="http://www.maizecdna.org/">The Maize Full Length cDNA Project
        </a>
    </strong>
    <div align="justify">
    This project uses genomics tools to understand a fundamental biological process through identificaion of genes expressed during maize reproduction and in somatic tissues responding to abiotic perturbations such as heat, cold, salt, UV-B, drought, and lack of light.</div>


    <br>
    <strong>
        <a href="http://www.maizegdb.org/maize_nomenclature.php">Maize Genetics Nomenclature Committee
        </a>
    </strong>

