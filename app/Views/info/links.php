<h2 class="wiki-section-header">
    Plant Transcription Factor Databases
</h2>  

    <br>
    <strong>
        <a href="http://planttfdb.cbi.pku.edu.cn/">PlantTFDB</a>
    </strong>
    <div align="justify"> The Plant Transcription Factor Database (PlantTFDB) is a comprehensive plant transcription factor (TF) database including about 20 species.
    </div>

    <br>
    <strong>
        <a href="http://plntfdb.bio.uni-potsdam.de/v3.0/">PlnTFDB
        </a>
    </strong>
    <div align="justify">PlnTFDB (3.0) is a public database arising from efforts to identify and catalogue all Plant genes involved in transcriptional control.
    </div>

